class CodeManager:
    """
    Manages generation, storage, and validation of one-time codes.

    Codes are stored in individual files named by a random request ID
    generated fresh for every authentication attempt -- never by the
    username or remote address. Two simultaneous connections from the
    same user and source (e.g. behind NAT) therefore always get distinct
    state and cannot overwrite or steal each other's code.

    File format is JSON with:
    - code: The plaintext OTP code
    - created: Unix timestamp of creation
    - expires: Unix timestamp of expiration
    - user: Username for verification
    - rhost: Remote host IP
    - attempts: Number of validation attempts made

    Security features:
    - Codes stored with 0600 permissions
    - Storage directory has 0700 permissions
    - Codes automatically expire
    - Codes are deleted after successful use
    - Failed attempts are tracked and limited
    - Request IDs are single-use, unguessable bearer tokens; validate()
      still cross-checks user/rhost as defense in depth

    Attributes:
        storage_dir (str): Directory for code storage files
        code_length (int): Length of generated codes
        timeout (int): Seconds until code expiration
        max_attempts (int): Maximum validation attempts allowed
        logger (PAMLogger): Logger instance for debugging

    Usage:
        manager = CodeManager("/var/run/pam-ssh-2fa", logger=logger)
        code, request_id = manager.generate("john", "192.168.1.1")
        is_valid, message = manager.validate(
            "john", "192.168.1.1", request_id, "1234"
        )
    """

    def __init__(
        self,
        storage_dir: str,
        code_length: int = 6,
        timeout: int = 300,
        max_attempts: int = 3,
        logger: Optional[PAMLogger] = None,
    ):
        """
        Initialize the code manager.

        Args:
            storage_dir: Directory to store code files
            code_length: Number of digits in generated codes
            timeout: Seconds until codes expire
            max_attempts: Max validation attempts before lockout
            logger: Optional logger for debug output
        """
        self.storage_dir = storage_dir
        self.code_length = code_length
        self.timeout = timeout
        self.max_attempts = max_attempts
        self.logger = logger

        # Ensure storage directory exists with secure permissions
        self._ensure_storage_dir()

    def _ensure_storage_dir(self):
        """
        Create the storage directory if it doesn't exist.

        Sets permissions to 0700 (owner read/write/execute only).
        This prevents other users from accessing pending codes.
        """
        if not os.path.exists(self.storage_dir):
            try:
                os.makedirs(self.storage_dir, mode=0o700)
            except OSError as e:
                if self.logger:
                    self.logger.error(f"Failed to create storage dir: {e}")
                raise
        else:
            # Ensure correct permissions even if dir already exists
            try:
                os.chmod(self.storage_dir, 0o700)
            except OSError:
                pass

    def _get_code_file(self, request_id: str) -> str:
        """
        Get the path to the code file for an authentication request.

        Args:
            request_id: Unique per-attempt request ID (see generate())

        Returns:
            Full path to the code file

        Raises:
            ValueError: If request_id does not match the expected token
                format. Malformed IDs are rejected outright rather than
                sanitized, so two different inputs can never resolve to
                the same file.
        """
        if not TOKEN_RE.fullmatch(request_id):
            raise ValueError("invalid request id")
        return os.path.join(self.storage_dir, f"code_{request_id}.json")

    def _write_atomic(self, code_file: str, code_data: dict):
        """
        Replace a code file's contents in one atomic filesystem operation.

        Writes to a temporary file in the same directory and renames it
        over the target, so a concurrent reader (or a crash mid-write)
        never observes a partially written or corrupt state file.

        Args:
            code_file: Path to the code file to replace
            code_data: JSON-serializable state to write
        """
        fd, temporary_path = tempfile.mkstemp(
            dir=self.storage_dir, prefix=".code-", text=True
        )
        try:
            os.fchmod(fd, 0o600)
            with os.fdopen(fd, "w") as f:
                json.dump(code_data, f)
                f.flush()
                os.fsync(f.fileno())
            os.replace(temporary_path, code_file)
        except Exception:
            try:
                os.unlink(temporary_path)
            except OSError:
                pass
            raise

    def generate(self, user: str, rhost: str) -> Tuple[str, str]:
        """
        Generate a new one-time code for a single authentication attempt.

        Creates a cryptographically secure random code and a unique
        request ID, then stores them with metadata for later validation.
        The request ID -- not the username or remote address -- names the
        state file, so concurrent attempts never collide.

        Args:
            user: Username requesting authentication
            rhost: Remote host IP address

        Returns:
            Tuple of (code, request_id). The caller must hold on to
            request_id and pass it back to validate().

        Raises:
            OSError: If unable to write the code file
        """
        # Generate cryptographically secure random code
        # Using secrets module for security-sensitive randomness
        code = "".join(str(secrets.randbelow(10)) for _ in range(self.code_length))

        # Unique bearer ID for this attempt only -- never reused, never
        # derived from user/rhost.
        request_id = secrets.token_urlsafe(32)

        # Calculate expiration time
        now = time.time()
        expires = now + self.timeout

        # Prepare code data
        code_data = {
            "code": code,
            "created": now,
            "expires": expires,
            "user": user,
            "rhost": rhost,
            "attempts": 0,
        }

        # Write to file with secure permissions
        code_file = self._get_code_file(request_id)

        # Create file with restricted permissions from the start. O_EXCL
        # guarantees we never silently overwrite another attempt's state,
        # even in the astronomically unlikely event of a token collision.
        fd = os.open(code_file, os.O_WRONLY | os.O_CREAT | os.O_EXCL, 0o600)
        try:
            with os.fdopen(fd, "w") as f:
                json.dump(code_data, f)
        except Exception:
            os.close(fd)
            raise

        if self.logger:
            # Log code generation (hash the code for security in logs)
            code_hash = hashlib.sha256(code.encode()).hexdigest()[:8]
            self.logger.debug(
                "Code generated",
                user=user,
                rhost=rhost,
                code_hash=code_hash,
                expires_in=f"{self.timeout}s",
            )

        return code, request_id

    def validate(
        self, user: str, rhost: str, request_id: str, entered_code: str
    ) -> Tuple[bool, str]:
        """
        Validate an entered code against the stored code.

        Checks if:
        - A code exists for this request ID
        - The stored user/rhost match this authentication attempt
        - The code hasn't expired
        - Maximum attempts haven't been exceeded
        - The entered code matches

        On successful validation, the code file is deleted.
        On failure, the attempt counter is incremented.

        The complete read/check/update/delete transition happens while
        holding an exclusive flock() on the code file, so two concurrent
        validate() calls for the same request ID can never both observe
        (and act on) the same pre-transition state -- see P0-1 in
        AUDIT_REMEDIATION_AND_ADMIN_PLAN.md, which found that the
        previous check-then-act version let two concurrent validations
        of one OTP both succeed. Holding the file open across the whole
        transition also lets a losing racer detect (via an inode
        comparison) that the file it opened was since replaced or
        deleted by the winner, instead of trusting stale in-memory data.

        Args:
            user: Username
            rhost: Remote host IP address
            request_id: The request ID returned by generate()
            entered_code: The code entered by the user

        Returns:
            Tuple of (success: bool, message: str)
            - success: True if code was valid
            - message: Human-readable result message
        """
        try:
            code_file = self._get_code_file(request_id)
        except ValueError:
            return False, "Invalid request. Please reconnect."

        try:
            fd = os.open(code_file, os.O_RDWR)
        except FileNotFoundError:
            if self.logger:
                self.logger.warning(
                    "No code found for validation", user=user, rhost=rhost
                )
            return False, "No pending code found. Please reconnect."
        except OSError as e:
            if self.logger:
                self.logger.error(f"Failed to open code file: {e}")
            return False, "Internal error. Please reconnect."

        try:
            fcntl.flock(fd, fcntl.LOCK_EX)
            try:
                return self._validate_locked(fd, code_file, user, rhost, entered_code)
            finally:
                fcntl.flock(fd, fcntl.LOCK_UN)
        finally:
            os.close(fd)

    def _validate_locked(
        self, fd: int, code_file: str, user: str, rhost: str, entered_code: str
    ) -> Tuple[bool, str]:
        """
        Perform the actual validation transition while fd is flock()ed.

        Must only be called with fd already holding an exclusive lock on
        code_file. Not part of the public API -- see validate().
        """
        # A racing call may have already consumed (unlinked) or replaced
        # this file between our open() and acquiring the lock. Comparing
        # the inode our fd refers to against what the path currently
        # resolves to detects that: unlink() doesn't invalidate an
        # already-open fd, so without this check we could still read and
        # act on stale, already-consumed data.
        try:
            path_stat = os.stat(code_file)
        except FileNotFoundError:
            return False, "No pending code found. Please reconnect."
        fd_stat = os.fstat(fd)
        if (path_stat.st_ino, path_stat.st_dev) != (fd_stat.st_ino, fd_stat.st_dev):
            return False, "No pending code found. Please reconnect."

        try:
            raw = os.read(fd, 1 << 20)
            code_data = json.loads(raw.decode("utf-8"))
            if not isinstance(code_data, dict):
                raise ValueError("code state is not an object")
        except (json.JSONDecodeError, UnicodeDecodeError, ValueError) as e:
            if self.logger:
                self.logger.error(f"Failed to read code file: {e}")
            self._unlink_locked(code_file)
            return False, "Internal error. Please reconnect."

        # Verify the code belongs to this user and source (defense in
        # depth -- the request ID alone already scopes the file to this
        # one attempt, but a mismatch here means something is very wrong).
        if code_data.get("user") != user or code_data.get("rhost") != rhost:
            if self.logger:
                self.logger.warning(
                    "User/rhost mismatch in code validation",
                    expected=f"{code_data.get('user')}:{code_data.get('rhost')}",
                    got=f"{user}:{rhost}",
                )
            return False, "Session mismatch. Please reconnect."

        expires = code_data.get("expires", 0)
        if not isinstance(expires, (int, float)) or time.time() > expires:
            if self.logger:
                self.logger.info("Code expired", user=user, rhost=rhost)
            self._unlink_locked(code_file)
            return False, "Code expired. Please reconnect to receive a new code."

        attempts = code_data.get("attempts", 0)
        if not isinstance(attempts, int):
            attempts = 0
        if attempts >= self.max_attempts:
            if self.logger:
                self.logger.warning(
                    "Max attempts exceeded", user=user, rhost=rhost, attempts=attempts
                )
            self._unlink_locked(code_file)
            return False, "Maximum attempts exceeded. Please reconnect."

        # Validate the code using constant-time comparison
        # This prevents timing attacks that could reveal code characters
        stored_code = code_data.get("code", "")
        if isinstance(stored_code, str) and secrets.compare_digest(
            entered_code.strip(), stored_code
        ):
            if self.logger:
                self.logger.info("Code validated successfully", user=user, rhost=rhost)
            self._unlink_locked(code_file)
            return True, "Verification successful."

        # Invalid code - increment attempt counter in place, on the same
        # fd/inode, so the lock and the staleness check above continue to
        # apply to whoever opens this path next.
        code_data["attempts"] = attempts + 1
        remaining = self.max_attempts - code_data["attempts"]

        try:
            encoded = json.dumps(code_data).encode("utf-8")
            os.lseek(fd, 0, os.SEEK_SET)
            os.ftruncate(fd, 0)
            os.write(fd, encoded)
            os.fsync(fd)
        except OSError:
            pass  # Best effort to save attempts

        if self.logger:
            self.logger.warning(
                "Invalid code entered",
                user=user,
                rhost=rhost,
                attempts=code_data["attempts"],
                remaining=remaining,
            )

        if remaining > 0:
            return False, f"Invalid code. {remaining} attempts remaining."
        else:
            self._unlink_locked(code_file)
            return False, "Invalid code. Maximum attempts exceeded."

    @staticmethod
    def _unlink_locked(code_file: str):
        """Remove a code file while its lock is still held by the caller."""
        try:
            os.unlink(code_file)
        except OSError:
            pass  # Best effort cleanup

    def cancel(self, request_id: str):
        """
        Best-effort removal of a code file that was never consumed by
        validate() -- e.g. the PAM conversation was cancelled, or
        notification delivery failed after the code was already written.
        Safe to call even if the file was already removed.

        Args:
            request_id: The request ID returned by generate()
        """
        try:
            code_file = self._get_code_file(request_id)
        except ValueError:
            return
        try:
            os.unlink(code_file)
        except OSError:
            pass  # Best effort cleanup

    def _cleanup_code(self, code_file: str):
        """
        Securely remove a code file.

        Args:
            code_file: Path to the code file to remove
        """
        try:
            os.unlink(code_file)
        except OSError:
            pass  # Best effort cleanup

    def cleanup_expired(self):
        """
        Remove all expired code files from storage.

        This can be called periodically (e.g., via cron) to clean up
        abandoned codes from interrupted sessions.
        """
        if not os.path.exists(self.storage_dir):
            return

        now = time.time()
        for filename in os.listdir(self.storage_dir):
            if not filename.startswith("code_"):
                continue

            filepath = os.path.join(self.storage_dir, filename)
            try:
                with open(filepath, "r") as f:
                    code_data = json.load(f)

                if now > code_data.get("expires", 0):
                    os.unlink(filepath)
                    if self.logger:
                        self.logger.debug(f"Cleaned up expired code: {filename}")
            except (json.JSONDecodeError, IOError, OSError):
                # Remove corrupt files
                try:
                    os.unlink(filepath)
                except OSError:
                    pass


