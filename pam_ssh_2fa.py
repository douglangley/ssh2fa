def pam_sm_authenticate(pamh, flags, argv):
    """
    PAM authentication entry point.

    This function is called by PAM when authentication is requested.
    It orchestrates the entire 2FA flow:

    1. Load configuration
    2. Get user and remote host info
    3. Check bypass conditions
    4. Check per-user/per-source/concurrency rate limits
    5. Generate and send OTP code
    6. Prompt user for code
    7. Validate response
    8. Return success or failure

    Args:
        pamh: PAM handle with user info and conversation functions
        flags: PAM flags (not used)
        argv: Module arguments from PAM config (not used)

    Returns:
        PAM_SUCCESS: Authentication successful, or 2FA bypassed
            (user/network in bypass list, or user unconfigured with
            allow_unconfigured_users=true)
        PAM_AUTH_ERR: Authentication failed
        PAM_AUTHINFO_UNAVAIL: Could not send notification
        PAM_MAXTRIES: Rate limit exceeded (per-user, per-source, or too
            many concurrent pending requests)
    """
    # -------------------------------------------------------------------------
    # Step 1: Load configuration
    # -------------------------------------------------------------------------
    config = Config(CONFIG_FILE)

    # Initialize logger
    logger = PAMLogger(
        config.get("log_file", "/var/log/pam-ssh-2fa.log"),
        debug=config.get("debug", False),
    )

    logger.debug("PAM authenticate called")

    # -------------------------------------------------------------------------
    # Step 2: Get user and session information
    # -------------------------------------------------------------------------
    try:
        user = pamh.get_user(None)
    except pamh.exception:
        logger.error("Failed to get username from PAM")
        return PAM_USER_UNKNOWN

    if not user:
        logger.error("Empty username")
        return PAM_USER_UNKNOWN

    # Get remote host from PAM environment
    # This is set by sshd when a user connects
    try:
        rhost = pamh.rhost or "unknown"
    except AttributeError:
        rhost = "unknown"

    logger.info("Authentication request", user=user, rhost=rhost)

    # -------------------------------------------------------------------------
    # Step 3: Check bypass conditions
    # -------------------------------------------------------------------------
    bypass_checker = BypassChecker(
        bypass_users=config.get_list("bypass_users"),
        bypass_networks=config.get_list("bypass_networks"),
        logger=logger,
    )

    if bypass_checker.should_bypass(user, rhost):
        # PAM_SUCCESS, not PAM_IGNORE. PAM_IGNORE tells the framework to
        # disregard this module's result and let some OTHER module in the
        # stack decide success/failure -- but the documented/recommended
        # PAM stack for this module (see examples/pam.d-sshd.example)
        # contains no other auth module, since SSH key verification
        # already happened at the OpenSSH layer before this stack ever
        # runs. With no other module to fall back on, PAM_IGNORE resolves
        # to an overall DENY, not a pass-through -- confirmed with
        # pamtester: a bypassed user got "Permission denied" here even
        # though should_bypass() correctly logged the bypass. PAM_SUCCESS
        # unambiguously grants access regardless of what else is (or
        # isn't) in the stack.
        return PAM_SUCCESS

    # -------------------------------------------------------------------------
    # Step 3.5: Load user-specific configuration
    # -------------------------------------------------------------------------
    # Check if this user has a personal notification config
    # This allows different users to receive codes via different services
    user_config = config.get_user_config(user)
    has_personal_config = config.has_user_config(user)

    if has_personal_config:
        logger.debug("Using per-user config", user=user)

    # -------------------------------------------------------------------------
    # Step 3.6: Check if user has notification URLs configured
    # -------------------------------------------------------------------------
    # User needs either a per-user config with URLs or global URLs must be set
    user_notification_urls = user_config.get_list("apprise_urls")

    if not user_notification_urls:
        # No notification URLs for this user - check allow_unconfigured_users
        allow_unconfigured = config.get("allow_unconfigured_users", False)

        if allow_unconfigured:
            # Allow unconfigured users to bypass 2FA
            logger.info(
                "User has no notification config - bypassing 2FA (allow_unconfigured_users=true)",
                user=user,
                rhost=rhost,
            )
            return PAM_SUCCESS
        else:
            # Deny unconfigured users
            logger.warning(
                "User denied - no notification URLs configured (allow_unconfigured_users=false)",
                user=user,
                rhost=rhost,
            )
            pam_error(pamh, "2FA not configured for this user. Contact administrator.")
            return PAM_AUTH_ERR

    # -------------------------------------------------------------------------
    # Step 4: Get auth method for this user
    # -------------------------------------------------------------------------
    auth_method = user_config.get("auth_method", "code").lower()

    # Validate auth_method
    if auth_method not in ("code", "link", "both", "none"):
        logger.warning(f"Invalid auth_method '{auth_method}', defaulting to 'code'")
        auth_method = "code"

    logger.debug("Auth method selected", user=user, auth_method=auth_method)

    # Handle "none" - skip 2FA for this user
    if auth_method == "none":
        logger.info("2FA skipped - auth_method=none", user=user, rhost=rhost)
        return PAM_SUCCESS

    # -------------------------------------------------------------------------
    # Step 4.5: Rate limiting
    # -------------------------------------------------------------------------
    # Gate here, before any code/approval state is created and before any
    # notification is sent, so a rate-limited attempt never causes a push
    # notification or consumes storage. One notification is sent per
    # allowed request, so limiting request creation also bounds
    # notification volume -- there is no separate cooldown to configure.
    code_storage_dir = config.get("code_storage_dir", "/var/run/pam-ssh-2fa")
    ratelimit_message = config.get(
        "ratelimit_message", "Too many attempts. Please wait a few minutes and try again."
    )
    rate_limiter = RateLimiter(
        storage_dir=os.path.join(code_storage_dir, "ratelimit"), logger=logger
    )

    allowed, retry_after = rate_limiter.check_window(
        "user",
        user,
        config.get("ratelimit_window", 300),
        config.get("ratelimit_max_per_user", 5),
    )
    if not allowed:
        logger.warning(
            "Rate limit exceeded for user",
            user=user,
            rhost=rhost,
            retry_after=retry_after,
        )
        pam_error(pamh, ratelimit_message)
        return PAM_MAXTRIES

    allowed, retry_after = rate_limiter.check_window(
        "rhost",
        rhost,
        config.get("ratelimit_window", 300),
        config.get("ratelimit_max_per_rhost", 15),
    )
    if not allowed:
        logger.warning(
            "Rate limit exceeded for source address",
            user=user,
            rhost=rhost,
            retry_after=retry_after,
        )
        pam_error(pamh, ratelimit_message)
        return PAM_MAXTRIES

    # Reserve one outstanding-request slot before creating any code or
    # approval state. reserve_request() replaces the previous
    # count-active-then-create pattern, which P0-2 in
    # AUDIT_REMEDIATION_AND_ADMIN_PLAN.md found was a check-then-act race
    # (multiple PAM processes could all observe "under the cap" and all
    # proceed). One lease covers the whole attempt regardless of
    # auth_method, so "both" -- which creates an OTP file AND an approval
    # file -- is counted once, not twice.
    code_timeout = config.get("code_timeout", 300)
    max_concurrent = config.get("ratelimit_max_concurrent_per_user", 3)
    lease_id = rate_limiter.reserve_request(user, code_timeout, max_concurrent)
    if lease_id is None:
        logger.warning(
            "Too many concurrent pending requests",
            user=user,
            rhost=rhost,
            max_concurrent=max_concurrent,
        )
        pam_error(pamh, ratelimit_message)
        return PAM_MAXTRIES

    # Everything from here on holds a reserved lease and may create
    # code/approval state. cleanup_state is populated by
    # _run_challenge() as it creates that state, so this outer finally
    # can release the lease and best-effort clean up on EVERY exit path
    # -- normal return, an early "return" inside _run_challenge, or an
    # unhandled exception -- not just the paths that already had their
    # own explicit cleanup call (see P0-3 in
    # AUDIT_REMEDIATION_AND_ADMIN_PLAN.md).
    cleanup_state = {}
    try:
        return _run_challenge(
            pamh,
            config,
            logger,
            user,
            rhost,
            auth_method,
            user_config,
            code_timeout,
            code_storage_dir,
            cleanup_state,
        )
    finally:
        rate_limiter.release_request(user, lease_id)
        _best_effort_cleanup(cleanup_state)


def _best_effort_cleanup(cleanup_state: dict):
    """
    Safety-net cleanup for any code/approval state _run_challenge() left
    behind on an exit path it didn't already handle explicitly (e.g. an
    unexpected exception). See P0-3 in AUDIT_REMEDIATION_AND_ADMIN_PLAN.md.

    Idempotent: CodeManager.cancel() and ApprovalManager.cleanup() are
    both safe to call on state that's already been consumed/removed by
    the normal code path, so calling this unconditionally is a harmless
    no-op in the common case.

    Args:
        cleanup_state: Dict populated by _run_challenge() with whichever
            of "code_manager"/"code_request_id" and
            "approval_manager"/"approval_token" were created
    """
    code_manager = cleanup_state.get("code_manager")
    code_request_id = cleanup_state.get("code_request_id")
    if code_manager and code_request_id:
        code_manager.cancel(code_request_id)

    approval_manager = cleanup_state.get("approval_manager")
    approval_token = cleanup_state.get("approval_token")
    if approval_manager and approval_token:
        approval_manager.cleanup(approval_token)


def _run_challenge(
    pamh,
    config,
    logger,
    user,
    rhost,
    auth_method,
    user_config,
    code_timeout,
    code_storage_dir,
    cleanup_state,
):
    """
    Steps 5-7 of pam_sm_authenticate(): generate credentials, send the
    notification, and run the interactive challenge loop for the given
    auth_method. Returns the final PAM_* return code.

    Extracted into its own function so pam_sm_authenticate can wrap the
    call in one try/finally that always releases the rate-limit lease
    and cleans up request state (see P0-2/P0-3 in
    AUDIT_REMEDIATION_AND_ADMIN_PLAN.md) -- including on an unhandled
    exception, which no amount of per-branch cleanup inside this
    function alone could guarantee.
    """
    # -------------------------------------------------------------------------
    # Step 5: Setup managers and generate credentials
    # -------------------------------------------------------------------------
    code = None
    code_request_id = None
    approval_token = None
    approval_link = None

    # Code manager (for "code" and "both" methods)
    code_manager = None
    if auth_method in ("code", "both"):
        code_manager = CodeManager(
            storage_dir=code_storage_dir,
            code_length=config.get("code_length", 6),
            timeout=code_timeout,
            max_attempts=config.get("max_attempts", 3),
            logger=logger,
        )

        try:
            code, code_request_id = code_manager.generate(user, rhost)
        except OSError as e:
            logger.error(f"Failed to generate code: {e}")
            pam_error(pamh, "Internal error. Please contact administrator.")
            return PAM_AUTHINFO_UNAVAIL
        cleanup_state["code_manager"] = code_manager
        cleanup_state["code_request_id"] = code_request_id

    # Approval manager (for "link" and "both" methods)
    approval_manager = None
    if auth_method in ("link", "both"):
        server_url = config.get("server_url", "")

        if not server_url:
            logger.error("server_url not configured but auth_method requires it")
            pam_error(
                pamh, "2FA link authentication not configured. Contact administrator."
            )
            return PAM_AUTHINFO_UNAVAIL

        approvals_dir = os.path.join(code_storage_dir, "approvals")

        approval_manager = ApprovalManager(
            approvals_dir=approvals_dir,
            timeout=code_timeout,
            server_url=server_url,
            logger=logger,
            allow_insecure_http=config.get("allow_insecure_http", False),
        )

        try:
            approval_token, approval_link = approval_manager.create_approval(
                user, rhost
            )
        except (OSError, ValueError) as e:
            logger.error(f"Failed to create approval: {e}")
            pam_error(pamh, "Internal error. Please contact administrator.")
            return PAM_AUTHINFO_UNAVAIL
        cleanup_state["approval_manager"] = approval_manager
        cleanup_state["approval_token"] = approval_token

    # -------------------------------------------------------------------------
    # Step 6: Send notification
    # -------------------------------------------------------------------------
    sender = NotificationSender(
        apprise_urls=user_config.get_list("apprise_urls"),
        title_template=user_config.get("notification_title", "SSH Login"),
        body_template=user_config.get("notification_body", "Your code: {code}"),
        body_template_link=user_config.get(
            "notification_body_link", "Click to approve: {link}"
        ),
        body_template_both=user_config.get(
            "notification_body_both", "Click: {link}\n\nOr code: {code}"
        ),
        logger=logger,
    )

    if not sender.send(
        code=code or "",
        user=user,
        rhost=rhost,
        timeout=code_timeout,
        link=approval_link,
        auth_method=auth_method,
    ):
        logger.error("Failed to send notification", user=user, rhost=rhost)
        pam_error(pamh, "Could not send verification. Please try again.")
        # Cleanup approval if created
        if approval_manager and approval_token:
            approval_manager.cleanup(approval_token)
        return PAM_AUTHINFO_UNAVAIL

    # -------------------------------------------------------------------------
    # Step 7: Authenticate based on method
    # -------------------------------------------------------------------------

    if auth_method == "link":
        # -----------------------------------------------------------------
        # Link-only authentication: poll for approval
        # -----------------------------------------------------------------
        pam_info(pamh, "Approval link sent to your device. Waiting for approval...")

        poll_interval = 1.0  # Check every second
        max_wait = code_timeout
        # monotonic(), not time(): see ApprovalManager.wait_for_approval()
        # -- this measures elapsed polling time in this one process and
        # must not be affected by a wall-clock adjustment mid-wait.
        start_time = time.monotonic()

        while True:
            # consume_approval() atomically checks-and-consumes in one
            # locked step (see P1-1 in AUDIT_REMEDIATION_AND_ADMIN_PLAN.md)
            # rather than a separate is_approved()+cleanup() pair.
            if approval_manager.consume_approval(approval_token):
                pam_info(
                    pamh, config.get("success_message", "Verification successful.")
                )
                logger.info("Authentication successful (link)", user=user, rhost=rhost)
                return PAM_SUCCESS

            # Check if expired
            if approval_manager.is_expired(approval_token):
                logger.warning("Approval expired", user=user, rhost=rhost)
                pam_error(pamh, config.get("expired_message", "Approval link expired."))
                return PAM_AUTH_ERR

            # Check overall timeout
            if time.monotonic() - start_time > max_wait:
                logger.warning("Approval timeout", user=user, rhost=rhost)
                pam_error(pamh, "Approval timeout. Please try again.")
                approval_manager.cleanup(approval_token)
                return PAM_AUTH_ERR

            time.sleep(poll_interval)

    elif auth_method == "both":
        # -----------------------------------------------------------------
        # Combined authentication: code OR link
        # -----------------------------------------------------------------
        prompt_msg = config.get(
            "prompt_message_both", "Enter code OR press Enter after clicking link: "
        )
        max_attempts = config.get("max_attempts", 3)

        for attempt in range(max_attempts):
            # Check if already approved via link before prompting
            if approval_manager.consume_approval(approval_token):
                pam_info(
                    pamh, config.get("success_message", "Verification successful.")
                )
                logger.info("Authentication successful (link)", user=user, rhost=rhost)
                return PAM_SUCCESS

            entered_code = pam_prompt(pamh, prompt_msg, echo=True)

            if entered_code is None:
                # User cancelled or connection issue
                logger.warning("User cancelled authentication", user=user, rhost=rhost)
                approval_manager.cleanup(approval_token)
                return PAM_AUTH_ERR

            # Empty input - check if link was clicked
            if entered_code.strip() == "":
                if approval_manager.consume_approval(approval_token):
                    pam_info(
                        pamh, config.get("success_message", "Verification successful.")
                    )
                    logger.info(
                        "Authentication successful (link)", user=user, rhost=rhost
                    )
                    return PAM_SUCCESS
                else:
                    if attempt < max_attempts - 1:
                        pam_error(
                            pamh, "Link not yet clicked. Enter code or try again."
                        )
                    continue

            # Non-empty input - validate as code
            valid, message = code_manager.validate(
                user, rhost, code_request_id, entered_code
            )

            if valid:
                pam_info(
                    pamh, config.get("success_message", "Verification successful.")
                )
                logger.info("Authentication successful (code)", user=user, rhost=rhost)
                approval_manager.cleanup(approval_token)
                return PAM_SUCCESS
            else:
                # Check if link was approved while they were typing
                if approval_manager.consume_approval(approval_token):
                    pam_info(
                        pamh, config.get("success_message", "Verification successful.")
                    )
                    logger.info(
                        "Authentication successful (link)", user=user, rhost=rhost
                    )
                    return PAM_SUCCESS

                if attempt < max_attempts - 1:
                    pam_error(pamh, message)
                else:
                    pam_error(
                        pamh, config.get("failure_message", "Verification failed.")
                    )

        # All attempts exhausted
        logger.warning("Authentication failed - max attempts", user=user, rhost=rhost)
        approval_manager.cleanup(approval_token)
        return PAM_AUTH_ERR

    else:
        # -----------------------------------------------------------------
        # Code-only authentication (default)
        # -----------------------------------------------------------------
        prompt_msg = config.get("prompt_message", "Enter verification code: ")
        max_attempts = config.get("max_attempts", 3)

        for attempt in range(max_attempts):
            entered_code = pam_prompt(pamh, prompt_msg, echo=True)

            if entered_code is None:
                # User cancelled or connection issue
                logger.warning("User cancelled authentication", user=user, rhost=rhost)
                return PAM_AUTH_ERR

            # Validate the code
            valid, message = code_manager.validate(
                user, rhost, code_request_id, entered_code
            )

            if valid:
                pam_info(
                    pamh, config.get("success_message", "Verification successful.")
                )
                logger.info("Authentication successful", user=user, rhost=rhost)
                return PAM_SUCCESS
            else:
                # Show error but continue if attempts remain
                if attempt < max_attempts - 1:
                    pam_error(pamh, message)
                else:
                    pam_error(
                        pamh, config.get("failure_message", "Verification failed.")
                    )

        # All attempts exhausted
        logger.warning("Authentication failed - max attempts", user=user, rhost=rhost)
        return PAM_AUTH_ERR


