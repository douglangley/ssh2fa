class RateLimiter:
    """
    File-based request-rate and concurrency limiter shared across PAM
    processes.

    Every authentication attempt calls check_window() once per identity
    (username, then source address) before any OTP code or approval is
    generated and before any notification is sent, so a rate-limited
    attempt never triggers a push notification or leaves state behind.
    count_active() enforces a separate cap on how many of one user's
    requests are outstanding at once.

    Sliding-window state lives in one JSON file per identity, holding a
    bounded list of recent Unix timestamps. An exclusive flock guards
    the read-modify-write cycle around that file, so two PAM processes
    racing to authenticate the same identity can't both read a stale
    count and both be let through.

    Attributes:
        storage_dir (str): Directory for rate-limit counter files
        logger (PAMLogger): Optional logger for debugging

    Usage:
        limiter = RateLimiter("/var/run/pam-ssh-2fa/ratelimit", logger=logger)
        allowed, retry_after = limiter.check_window("user", "john", 300, 5)
        if not allowed:
            ...  # deny, tell the user to wait retry_after seconds
    """

    def __init__(self, storage_dir: str, logger: Optional[PAMLogger] = None):
        """
        Initialize the rate limiter.

        Args:
            storage_dir: Directory to store per-identity counter files
            logger: Optional logger for debug output
        """
        self.storage_dir = storage_dir
        self.logger = logger
        self._ensure_dir()

    def _ensure_dir(self):
        """Create the counter storage directory with secure permissions."""
        if not os.path.exists(self.storage_dir):
            try:
                os.makedirs(self.storage_dir, mode=0o700)
            except OSError as e:
                if self.logger:
                    self.logger.error(f"Failed to create ratelimit dir: {e}")
                raise
        else:
            try:
                os.chmod(self.storage_dir, 0o700)
            except OSError:
                pass

    def _state_file(self, kind: str, identity: str) -> str:
        """
        Get the counter file path for an identity.

        The identity (a username or an IP address) is hashed rather
        than used verbatim, consistent with how other state directories
        in this module avoid leaking usernames via directory listings.

        Args:
            kind: Namespace for the identity, e.g. "user" or "rhost"
            identity: The value being rate limited

        Returns:
            Full path to the counter file
        """
        digest = hashlib.sha256(f"{kind}:{identity}".encode()).hexdigest()[:16]
        return os.path.join(self.storage_dir, f"{kind}_{digest}.json")

    def check_window(
        self,
        kind: str,
        identity: str,
        window_seconds: int,
        max_events: int,
        now: Optional[float] = None,
    ) -> Tuple[bool, int]:
        """
        Atomically check and record one event against a sliding window.

        Args:
            kind: Namespace for the identity, e.g. "user" or "rhost"
            identity: The value being rate limited
            window_seconds: Width of the sliding window in seconds
            max_events: Maximum events allowed inside the window
            now: Override for the current time (used by tests)

        Returns:
            Tuple of (allowed, retry_after_seconds). If allowed is
            False, the event was NOT recorded -- the caller must not
            proceed with the action being limited.
        """
        now = now if now is not None else time.time()
        path = self._state_file(kind, identity)

        fd = os.open(path, os.O_RDWR | os.O_CREAT, 0o600)
        try:
            fcntl.flock(fd, fcntl.LOCK_EX)
            try:
                raw = os.read(fd, 1 << 16)
                try:
                    events = json.loads(raw.decode("utf-8")) if raw else []
                    if not isinstance(events, list):
                        events = []
                except (json.JSONDecodeError, UnicodeDecodeError):
                    events = []

                cutoff = now - window_seconds
                events = [
                    t for t in events if isinstance(t, (int, float)) and t > cutoff
                ]

                if len(events) >= max_events:
                    retry_after = max(1, int(min(events) + window_seconds - now) + 1)
                    if self.logger:
                        self.logger.debug(
                            "Rate limit window full",
                            kind=kind,
                            count=len(events),
                            max_events=max_events,
                            retry_after=retry_after,
                        )
                    return False, retry_after

                events.append(now)

                encoded = json.dumps(events).encode("utf-8")
                os.lseek(fd, 0, os.SEEK_SET)
                os.ftruncate(fd, 0)
                os.write(fd, encoded)
                return True, 0
            finally:
                fcntl.flock(fd, fcntl.LOCK_UN)
        finally:
            os.close(fd)

    def count_active(
        self, directory: str, prefix: str, user: str, now: Optional[float] = None
    ) -> int:
        """
        Count non-expired request state files belonging to a user.

        Scans an existing OTP or approval storage directory rather than
        keeping a separate counter, so the count is always consistent
        with what's actually outstanding -- it can't drift the way a
        manually incremented/decremented counter could if a code is
        never explicitly cleaned up (e.g. the SSH client disconnects).

        Args:
            directory: Directory containing JSON state files (code or
                approval storage)
            prefix: Only filenames starting with this prefix are
                considered (e.g. "code_"; use "" for approval files)
            user: Username to match against each file's "user" field
            now: Override for the current time (used by tests)

        Returns:
            Number of matching, unexpired files found. Best-effort --
            unreadable or corrupt files are skipped, not counted.
        """
        now = now if now is not None else time.time()
        if not os.path.isdir(directory):
            return 0

        try:
            entries = os.listdir(directory)
        except OSError:
            return 0

        count = 0
        for filename in entries:
            if not filename.startswith(prefix) or not filename.endswith(".json"):
                continue
            filepath = os.path.join(directory, filename)
            try:
                with open(filepath, "r") as f:
                    data = json.load(f)
            except (OSError, json.JSONDecodeError):
                continue
            if data.get("user") != user:
                continue
            if now > data.get("expires", 0):
                continue
            count += 1

        return count

    def _lease_file(self, user: str) -> str:
        """Get the lease-list file path for a user (see reserve_request)."""
        digest = hashlib.sha256(f"lease:{user}".encode()).hexdigest()[:16]
        return os.path.join(self.storage_dir, f"lease_{digest}.json")

    def reserve_request(
        self,
        user: str,
        ttl_seconds: int,
        limit: int,
        now: Optional[float] = None,
    ) -> Optional[str]:
        """
        Atomically reserve one outstanding-request slot for a user, or
        refuse if the user is already at the concurrency cap.

        This replaces the previous scan-then-create pattern (count how
        many OTP/approval files exist, then separately create a new one)
        that P0-2 in AUDIT_REMEDIATION_AND_ADMIN_PLAN.md found was a
        check-then-act race: multiple PAM processes could all observe
        "under the cap" and all proceed, since nothing reserved the slot
        between the check and the file creation. Here, the prune/count/
        reserve sequence happens under one exclusive flock on a single
        per-user lease-list file, so the cap can never be exceeded no
        matter how many processes race to reserve at once.

        The caller must call generate()/create_approval() only AFTER a
        successful reservation, and must call release_request() exactly
        once when the attempt concludes (success, failure, error, or
        cancellation) -- typically from a `finally` block. One lease
        covers one authentication attempt regardless of auth_method, so
        `both` (which creates both an OTP file and an approval file) is
        counted once, not twice.

        Args:
            user: Username the reservation is scoped to
            ttl_seconds: How long the lease is valid for if never
                explicitly released (matches the OTP/approval timeout,
                so an abandoned attempt -- e.g. a dropped SSH connection
                -- stops counting once its own state would have expired
                anyway)
            limit: Maximum concurrent outstanding leases for this user
            now: Override for the current time (used by tests)

        Returns:
            A lease ID to pass to release_request(), or None if the
            user is already at the concurrency cap.
        """
        now = now if now is not None else time.time()
        path = self._lease_file(user)

        fd = os.open(path, os.O_RDWR | os.O_CREAT, 0o600)
        try:
            fcntl.flock(fd, fcntl.LOCK_EX)
            try:
                leases = self._read_leases(fd, now)

                if len(leases) >= limit:
                    if self.logger:
                        self.logger.debug(
                            "Concurrent-request cap reached",
                            user=user,
                            active=len(leases),
                            limit=limit,
                        )
                    self._write_leases(fd, leases)
                    return None

                lease_id = secrets.token_urlsafe(16)
                leases.append({"id": lease_id, "expires": now + ttl_seconds})
                self._write_leases(fd, leases)
                return lease_id
            finally:
                fcntl.flock(fd, fcntl.LOCK_UN)
        finally:
            os.close(fd)

    def release_request(self, user: str, lease_id: str, now: Optional[float] = None):
        """
        Release a lease acquired by reserve_request().

        Safe to call even if the lease already expired or the lease file
        doesn't exist (e.g. this is the first call for a user). Must be
        called exactly once per successful reserve_request() call,
        typically from a `finally` block, so a completed or abandoned
        attempt frees its slot for the next one.

        Args:
            user: Username the reservation was scoped to
            lease_id: The lease ID returned by reserve_request()
            now: Override for the current time (used by tests)
        """
        if not lease_id:
            return
        now = now if now is not None else time.time()
        path = self._lease_file(user)

        try:
            fd = os.open(path, os.O_RDWR)
        except FileNotFoundError:
            return

        try:
            fcntl.flock(fd, fcntl.LOCK_EX)
            try:
                leases = self._read_leases(fd, now)
                leases = [lease for lease in leases if lease.get("id") != lease_id]
                self._write_leases(fd, leases)
            finally:
                fcntl.flock(fd, fcntl.LOCK_UN)
        finally:
            os.close(fd)

    @staticmethod
    def _read_leases(fd: int, now: float) -> list:
        """Read a lease-list file (already open+locked) and prune expired entries."""
        os.lseek(fd, 0, os.SEEK_SET)
        raw = os.read(fd, 1 << 16)
        try:
            leases = json.loads(raw.decode("utf-8")) if raw else []
            if not isinstance(leases, list):
                leases = []
        except (json.JSONDecodeError, UnicodeDecodeError):
            leases = []
        return [
            lease
            for lease in leases
            if isinstance(lease, dict)
            and isinstance(lease.get("expires"), (int, float))
            and lease["expires"] > now
        ]

    @staticmethod
    def _write_leases(fd: int, leases: list):
        """Write a lease-list file (already open+locked) in place."""
        encoded = json.dumps(leases).encode("utf-8")
        os.lseek(fd, 0, os.SEEK_SET)
        os.ftruncate(fd, 0)
        os.write(fd, encoded)
        os.fsync(fd)


