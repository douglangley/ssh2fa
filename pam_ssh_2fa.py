class PAMLogger:
    """
    Custom logger for PAM module that writes to both file and syslog.

    This class provides a unified logging interface that:
    - Writes to a dedicated log file for easy debugging
    - Also logs to syslog for system integration
    - Supports debug mode for verbose output
    - Sanitizes sensitive data before logging

    Attributes:
        log_file (str): Path to the log file
        debug (bool): Whether debug logging is enabled
        logger (logging.Logger): The underlying Python logger instance

    Usage:
        logger = PAMLogger("/var/log/pam-ssh-2fa.log", debug=True)
        logger.info("User authenticated", user="john", rhost="192.168.1.1")
        logger.debug("Code generated", code_hash="abc123")  # Only if debug=True
    """

    def __init__(self, log_file: str, debug: bool = False):
        """
        Initialize the PAM logger.

        Args:
            log_file: Path to the log file. Directory will be created if needed.
            debug: If True, DEBUG level messages will be logged.
        """
        self.log_file = log_file
        # Named distinctly from the debug() method below -- an attribute
        # and a method can't share a name on the same instance without one
        # shadowing the other.
        self.debug_enabled = debug
        self.logger = logging.getLogger("pam-ssh-2fa")
        self.logger.setLevel(logging.DEBUG if debug else logging.INFO)
        # Don't propagate to the root logger -- PAM modules can be
        # constructed repeatedly within one long-lived process (each
        # authentication attempt makes a fresh PAMLogger), and without
        # this every message would also be emitted by whatever handlers
        # end up on the root logger, duplicating output.
        self.logger.propagate = False

        # Close any existing handlers before clearing them. handlers.clear()
        # alone drops the references without closing the underlying file/
        # socket descriptors, which showed up as ResourceWarnings for
        # unclosed log files and syslog sockets under repeated
        # construction (e.g. one PAMLogger per test, or per PAM
        # invocation in a long-lived process) -- see P1-8 in
        # AUDIT_REMEDIATION_AND_ADMIN_PLAN.md.
        for handler in self.logger.handlers:
            try:
                handler.close()
            except Exception:
                pass
        self.logger.handlers.clear()

        # Create log directory if it doesn't exist
        log_dir = os.path.dirname(log_file)
        if log_dir and not os.path.exists(log_dir):
            try:
                os.makedirs(log_dir, mode=0o750)
            except OSError:
                pass  # Fall back to syslog only

        # File handler with detailed format
        try:
            file_handler = logging.FileHandler(log_file)
            file_handler.setFormatter(
                logging.Formatter(
                    "%(asctime)s [%(levelname)s] %(message)s",
                    datefmt="%Y-%m-%d %H:%M:%S",
                )
            )
            self.logger.addHandler(file_handler)
        except (OSError, IOError):
            pass  # Fall back to syslog only

        # Syslog handler for system integration
        try:
            from logging.handlers import SysLogHandler

            syslog_handler = SysLogHandler(
                address="/dev/log", facility=SysLogHandler.LOG_AUTH
            )
            syslog_handler.setFormatter(logging.Formatter("pam-ssh-2fa: %(message)s"))
            self.logger.addHandler(syslog_handler)
        except (OSError, IOError):
            pass  # Syslog not available

    def _format_message(self, message: str, **kwargs) -> str:
        """
        Format a log message with additional context.

        Args:
            message: The main log message
            **kwargs: Additional key-value pairs to include

        Returns:
            Formatted message string with context appended
        """
        if kwargs:
            context = " | ".join(f"{k}={v}" for k, v in kwargs.items())
            return f"{message} | {context}"
        return message

    def info(self, message: str, **kwargs):
        """Log an INFO level message."""
        self.logger.info(self._format_message(message, **kwargs))

    def warning(self, message: str, **kwargs):
        """Log a WARNING level message."""
        self.logger.warning(self._format_message(message, **kwargs))

    def error(self, message: str, **kwargs):
        """Log an ERROR level message."""
        self.logger.error(self._format_message(message, **kwargs))

    def debug(self, message: str, **kwargs):
        """Log a DEBUG level message (only if debug mode is enabled)."""
        if self.debug_enabled:
            self.logger.debug(self._format_message(message, **kwargs))

    def close(self):
        """
        Close and detach this logger's handlers.

        Idempotent -- safe to call multiple times, and safe even if a
        later PAMLogger construction has already replaced these handlers
        on the shared "pam-ssh-2fa" logger (each handler is closed and
        removed by reference, not by looking the logger up again).
        """
        for handler in list(self.logger.handlers):
            try:
                handler.close()
            except Exception:
                pass
            try:
                self.logger.removeHandler(handler)
            except Exception:
                pass


