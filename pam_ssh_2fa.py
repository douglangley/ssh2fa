class ApprovalManager:
    """
    Manages link-based authentication approvals.

    When auth_method is 'link' or 'both', this manager creates approval
    request files that the approval server monitors. When a user clicks
    the approval link, the server marks the file as approved.

    Approval files are JSON with structure:
    {
        "token": "abc123...",
        "user": "username",
        "rhost": "192.168.1.1",
        "host": "servername",
        "created": 1234567890.123,
        "expires": 1234567890.123,
        "approved": false,
        "approved_at": null
    }

    Attributes:
        approvals_dir (str): Directory for approval request files
        timeout (int): Seconds until approval expires
        server_url (str): Base URL for approval links
        logger (PAMLogger): Optional logger for debugging

    Usage:
        manager = ApprovalManager(
            approvals_dir="/var/run/pam-ssh-2fa/approvals",
            timeout=300,
            server_url="http://myserver.com:9110",
            logger=logger
        )
        token, link = manager.create_approval("john", "192.168.1.1")
        # Send link to user...
        # Poll for approval:
        while not manager.is_approved(token):
            time.sleep(1)
    """

    def __init__(
        self,
        approvals_dir: str,
        timeout: int = 300,
        server_url: str = "",
        logger: Optional[PAMLogger] = None,
        allow_insecure_http: bool = False,
    ):
        """
        Initialize the approval manager.

        Args:
            approvals_dir: Directory to store approval files
            timeout: Seconds until approvals expire
            server_url: Base URL for approval server (e.g., http://myserver:9110)
            logger: Optional logger for debug output
            allow_insecure_http: If True, permit a plain http:// server_url
                even for a public (non-loopback, non-private) host. Off by
                default -- approval links are bearer credentials, so an
                http:// link to a public host can be approved by anyone who
                observes it in transit.
        """
        self.approvals_dir = approvals_dir
        self.timeout = timeout
        self.server_url = server_url.rstrip("/") if server_url else ""
        self.logger = logger
        self.allow_insecure_http = allow_insecure_http

        # Ensure approvals directory exists
        self._ensure_dir()

    def _ensure_dir(self):
        """Create the approvals directory if it doesn't exist."""
        if not os.path.exists(self.approvals_dir):
            try:
                os.makedirs(self.approvals_dir, mode=0o700)
            except OSError as e:
                if self.logger:
                    self.logger.error(f"Failed to create approvals dir: {e}")
                raise
        else:
            # Ensure correct permissions
            try:
                os.chmod(self.approvals_dir, 0o700)
            except OSError:
                pass

    def _get_approval_file(self, token: str) -> str:
        """Get the path to an approval file by token."""
        # Reject malformed tokens instead of changing them. Changing a bearer
        # token can make two different inputs address the same request.
        if not TOKEN_RE.fullmatch(token):
            raise ValueError("invalid approval token")
        return os.path.join(self.approvals_dir, f"{token}.json")

    @staticmethod
    def _is_non_global_host(host: str) -> bool:
        """
        Decide whether a URL host is confined to a trusted network.

        Only literal IP addresses can be classified this way without a
        DNS lookup (which this module never performs at config-parse
        time -- adding a network call to a privileged auth path is
        exactly the kind of risk this project is trying to remove, see
        MODERNIZATION_PLAN.md). A bare hostname is therefore treated as
        public/untrusted even if an admin's private DNS happens to
        resolve it internally -- "localhost" is special-cased since
        it's a universal loopback convention rather than a real DNS
        name.

        is_global is used rather than is_private so this also covers
        Tailscale/CGNAT addresses (100.64.0.0/10, RFC 6598 "Shared
        Address Space"), which Python's ipaddress module does not
        consider is_private but which are not publicly routable either.

        Args:
            host: The hostname or IP literal from a parsed URL

        Returns:
            True if the host is not routable on the public internet
        """
        if not host:
            return False
        if host == "localhost":
            return True
        try:
            addr = ipaddress.ip_address(host)
        except ValueError:
            return False  # A real hostname -- can't vouch for it
        return not addr.is_global

    def _check_server_url_security(self):
        """
        Reject an insecure server_url before it's embedded in a link.

        An approval link is a bearer credential: whoever observes it in
        transit can approve the associated SSH login. A plain http://
        link to a public host is therefore rejected unless the operator
        explicitly opts out via allow_insecure_http. Loopback and
        private-network deployments are exempt, since the link never
        leaves a trusted network in that case.

        Raises:
            ValueError: If the URL's scheme is unsupported, or if it is
                an insecure http:// URL to a public host and
                allow_insecure_http is not set
        """
        parsed = urlparse(self.server_url)

        if parsed.scheme == "https":
            return

        if parsed.scheme != "http":
            raise ValueError(
                f"server_url must use http:// or https:// (got {parsed.scheme!r})"
            )

        if self._is_non_global_host(parsed.hostname or ""):
            return

        if self.allow_insecure_http:
            if self.logger:
                self.logger.warning(
                    "Sending approval link over plain HTTP to a public host",
                    host=parsed.hostname,
                )
            return

        raise ValueError(
            "server_url uses http:// for a public host "
            f"({parsed.hostname!r}); set [server] url to an https:// "
            "address, deploy behind a TLS reverse proxy, or set "
            "[server] allow_insecure_http = true to override "
            "(not recommended -- see MODERNIZATION_PLAN.md)"
        )

    def create_approval(self, user: str, rhost: str) -> Tuple[str, str]:
        """
        Create a new approval request.

        Args:
            user: Username requesting authentication
            rhost: Remote host IP address

        Returns:
            Tuple of (token, approval_link)

        Raises:
            OSError: If unable to write the approval file
            ValueError: If server_url is not configured, or is an
                insecure http:// URL to a public host (see
                _check_server_url_security)
        """
        if not self.server_url:
            raise ValueError("server_url not configured - cannot create approval links")

        self._check_server_url_security()

        # Generate cryptographically secure random token
        token = secrets.token_urlsafe(32)

        # Get hostname
        try:
            hostname = os.uname().nodename
        except (AttributeError, OSError):
            hostname = "unknown"

        # Calculate expiration
        now = time.time()
        expires = now + self.timeout

        # Prepare approval data
        approval_data = {
            "token": token,
            # A link preview may safely GET the page, but approval requires an
            # explicit POST containing this independent confirmation secret.
            "confirmation_token": secrets.token_urlsafe(32),
            "user": user,
            "rhost": rhost,
            "host": hostname,
            "created": now,
            "expires": expires,
            "approved": False,
            "approved_at": None,
        }

        # Write to file with secure permissions
        approval_file = self._get_approval_file(token)

        fd = os.open(approval_file, os.O_WRONLY | os.O_CREAT | os.O_TRUNC, 0o600)
        try:
            with os.fdopen(fd, "w") as f:
                json.dump(approval_data, f)
        except Exception:
            os.close(fd)
            raise

        # Construct approval link
        approval_link = f"{self.server_url}/approve/{token}"

        if self.logger:
            self.logger.debug(
                "Approval request created",
                user=user,
                rhost=rhost,
                token=token[:8] + "...",
                expires_in=f"{self.timeout}s",
            )

        return token, approval_link

    def is_approved(self, token: str) -> bool:
        """
        Check if an approval has been granted.

        Args:
            token: The approval token

        Returns:
            True if approved, False otherwise
        """
        try:
            approval_file = self._get_approval_file(token)
        except ValueError:
            return False

        if not os.path.exists(approval_file):
            return False

        try:
            with open(approval_file, "r") as f:
                data = json.load(f)

            # Check if expired
            if time.time() > data.get("expires", 0):
                return False

            return data.get("approved", False)

        except (json.JSONDecodeError, IOError):
            return False

    def is_expired(self, token: str) -> bool:
        """
        Check if an approval request has expired.

        Args:
            token: The approval token

        Returns:
            True if expired or not found, False if still valid
        """
        try:
            approval_file = self._get_approval_file(token)
        except ValueError:
            return True

        if not os.path.exists(approval_file):
            return True

        try:
            with open(approval_file, "r") as f:
                data = json.load(f)

            return time.time() > data.get("expires", 0)

        except (json.JSONDecodeError, IOError):
            return True

    def cleanup(self, token: str):
        """
        Remove an approval request file.

        Args:
            token: The approval token to clean up
        """
        try:
            approval_file = self._get_approval_file(token)
        except ValueError:
            return
        try:
            os.unlink(approval_file)
        except OSError:
            pass

    def consume_approval(self, token: str) -> bool:
        """
        Atomically check whether an approval has been granted and, if
        so, consume it (delete the file) in one locked operation.

        This is the single decision point PAM should use to grant
        access -- prefer it over is_approved() followed by a separate
        cleanup() call, which is not atomic: see P1-1 in
        AUDIT_REMEDIATION_AND_ADMIN_PLAN.md. An exclusive flock plus an
        inode-staleness check (the same technique as
        CodeManager._validate_locked()) means a losing racer can never
        observe "approved" on a request another caller already consumed.

        Args:
            token: The approval token

        Returns:
            True if the approval was granted and has now been consumed
            (the caller should treat this as success and not call
            cleanup() again). False if not found, not yet approved, or
            expired.
        """
        try:
            approval_file = self._get_approval_file(token)
        except ValueError:
            return False

        try:
            fd = os.open(approval_file, os.O_RDWR)
        except FileNotFoundError:
            return False
        except OSError as e:
            if self.logger:
                self.logger.error(f"Failed to open approval file: {e}")
            return False

        try:
            fcntl.flock(fd, fcntl.LOCK_EX)
            try:
                try:
                    path_stat = os.stat(approval_file)
                except FileNotFoundError:
                    return False
                fd_stat = os.fstat(fd)
                if (path_stat.st_ino, path_stat.st_dev) != (
                    fd_stat.st_ino,
                    fd_stat.st_dev,
                ):
                    return False

                try:
                    raw = os.read(fd, 1 << 20)
                    data = json.loads(raw.decode("utf-8"))
                    if not isinstance(data, dict):
                        raise ValueError("approval state is not an object")
                except (json.JSONDecodeError, UnicodeDecodeError, ValueError):
                    return False

                expires = data.get("expires", 0)
                if not isinstance(expires, (int, float)) or time.time() > expires:
                    try:
                        os.unlink(approval_file)
                    except OSError:
                        pass
                    return False

                if not data.get("approved", False):
                    return False

                try:
                    os.unlink(approval_file)
                except OSError:
                    pass
                return True
            finally:
                fcntl.flock(fd, fcntl.LOCK_UN)
        finally:
            os.close(fd)

    def wait_for_approval(
        self, token: str, poll_interval: float = 1.0, timeout: Optional[int] = None
    ) -> bool:
        """
        Wait for an approval to be granted.

        Polls the approval file until approved or timeout.

        Args:
            token: The approval token
            poll_interval: Seconds between polls (default 1.0)
            timeout: Max seconds to wait (default: use approval timeout)

        Returns:
            True if approved, False if timeout/expired
        """
        if timeout is None:
            timeout = self.timeout

        # monotonic(), not time(): this measures how long THIS process
        # has been polling, which must not be affected by a wall-clock
        # adjustment (NTP step, manual clock change) during the wait.
        # The approval file's own "expires" field is compared elsewhere
        # using wall-clock time, which is correct there since it's a
        # cross-process, cross-time value.
        start_time = time.monotonic()

        while True:
            # Check if approved
            if self.is_approved(token):
                return True

            # Check if expired
            if self.is_expired(token):
                return False

            # Check overall timeout
            if time.monotonic() - start_time > timeout:
                return False

            # Wait before next poll
            time.sleep(poll_interval)


